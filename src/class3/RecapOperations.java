package class3;


public class RecapOperations {

	public static void main(String[] args) {
		
		/*addition= +
		 * subtraction= -
		 * mult= *
		 * division= \
		 * modulus= %
		 */
		
		int number1=11;
		int number2=3;
		System.out.println("Answer = "+number1/number2); //dividing number1 by number2
		System.out.println("Remainder = "+ number1%number2);
		
		double number3=9;
		double number4=2;
		System.out.println(number3/number4); //dividing number1 by number2
		
		double number5=1;
		double number6=3;
		System.out.println(number5/number6);
		
		float number7=1.0f;
		float number8=3.0f;
		System.out.println(number7/number8);
				


	}

}
