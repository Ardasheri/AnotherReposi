public class ElseIf7 {

	public static void main(String[] args) {
		
		int age =75;
		
		if(age==75) {
			System.out.println("Ok boomer");
		}
		else if (age>=18) {
			System.out.println("Your an adult");
		}
		else if(age>=13) {
			System.out.println("You are a teen");
		}
		else {
			System.out.println("You are not an adult");
		}
				
		
	}
}

